#include "libft.h"
#include <stdio.h>

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (c);
	if (c >= '0' && c <= '9')
		return (c);
	else
		return (0);
}
